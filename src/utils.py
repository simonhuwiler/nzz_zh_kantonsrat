from dateutil import parser

def kantonsrat_to_datetime(kantonsrat):
    for k in kantonsrat:
        for x in k['einsitz']:
            x['start'] = parser.parse(x['start'])
            x['end'] = parser.parse(x['end'])
        for x in k['partei']:
            x['start'] = parser.parse(x['start'])
            x['end'] = parser.parse(x['end'])
        for x in k['funktion']:
            x['start'] = parser.parse(x['start'])
            x['end'] = parser.parse(x['end'])    

